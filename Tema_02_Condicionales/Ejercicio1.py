print("Este programa te dice si el numero 1 es mayor que el numero  2")
num1 = int(input("Introduce el numero 1, por favor! "))
num2 = int(input("Introduce el numero 2, por favor! "))

if num1 > num2:
    print(f"El numero 1 ({num1}) es mayor que el numero 2 ({num2})")

else:
    print(f"El numero 2 ({num2}) es mayor que el numero 1 ({num1})")
